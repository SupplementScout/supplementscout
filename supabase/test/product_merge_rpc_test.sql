create or replace function public.assert_merge_blocked(
  canonical_id bigint,
  candidate_id bigint,
  expected_message text
)
returns void
language plpgsql
as $$
begin
  perform public.merge_products(canonical_id, candidate_id);
  raise exception 'Expected merge_products(%, %) to fail with: %',
    canonical_id,
    candidate_id,
    expected_message;
exception
  when others then
    if sqlerrm not like expected_message then
      raise exception 'Unexpected error for merge_products(%, %). Expected like "%", got "%"',
        canonical_id,
        candidate_id,
        expected_message,
        sqlerrm;
    end if;
end;
$$;

-- Test: safe merge.
do $outer$
declare
  history_record public.product_merge_history%rowtype;
begin
  begin
    perform public.merge_products(1001::bigint, 1002::bigint);

    if not exists (
      select 1
      from public.products
      where id = 1002
        and is_active = false
        and merged_into_product_id = 1001
        and merged_at is not null
    ) then
      raise exception 'Safe merge failed: candidate product was not marked merged into canonical';
    end if;

    if not exists (
      select 1
      from public.offers
      where id = 2002
        and product_id = 1001
    ) then
      raise exception 'Safe merge failed: candidate offer was not moved to canonical';
    end if;

    if not exists (
      select 1
      from public.retailer_products
      where id = 4002
        and product_id = 1001
    ) then
      raise exception 'Safe merge failed: candidate retailer_products mapping was not moved to canonical';
    end if;

    if (
      select count(*)
      from public.price_history
      where id in (3001, 3002)
        and offer_id = 2002
    ) <> 2 then
      raise exception 'Safe merge failed: expected two preserved price_history rows for offer 2002';
    end if;

    select *
    into history_record
    from public.product_merge_history
    where canonical_product_id = 1001
      and candidate_product_id = 1002;

    if history_record.id is null then
      raise exception 'Safe merge failed: product_merge_history row was not created';
    end if;

    if history_record.offers_moved <> 1 then
      raise exception 'Safe merge failed: offers_moved expected 1, got %', history_record.offers_moved;
    end if;

    if history_record.retailer_products_moved <> 1 then
      raise exception 'Safe merge failed: retailer_products_moved expected 1, got %', history_record.retailer_products_moved;
    end if;

    if history_record.price_history_preserved <> 2 then
      raise exception 'Safe merge failed: price_history_preserved expected 2, got %', history_record.price_history_preserved;
    end if;

    if history_record.candidate_offer_ids <> array[2002]::bigint[] then
      raise exception 'Safe merge failed: candidate_offer_ids snapshot mismatch';
    end if;

    if history_record.moved_offer_ids <> array[2002]::bigint[] then
      raise exception 'Safe merge failed: moved_offer_ids mismatch';
    end if;

    if history_record.moved_retailer_product_ids <> array[4002]::bigint[] then
      raise exception 'Safe merge failed: moved_retailer_product_ids mismatch';
    end if;

    if history_record.price_history_offer_ids <> array[2002]::bigint[] then
      raise exception 'Safe merge failed: price_history_offer_ids mismatch';
    end if;

    if history_record.snapshot->'candidate_before_merge'->>'id' <> '1002' then
      raise exception 'Safe merge failed: snapshot missing candidate_before_merge';
    end if;

    if history_record.snapshot->'canonical_before_merge'->>'id' <> '1001' then
      raise exception 'Safe merge failed: snapshot missing canonical_before_merge';
    end if;

    if jsonb_array_length(history_record.snapshot->'candidate_offers_before') <> 1 then
      raise exception 'Safe merge failed: candidate_offers_before should contain one row';
    end if;

    if history_record.snapshot->'candidate_offers_before'->0->>'product_id' <> '1002' then
      raise exception 'Safe merge failed: candidate_offers_before did not preserve pre-update product_id';
    end if;

    if jsonb_array_length(history_record.snapshot->'candidate_retailer_products_before') <> 1 then
      raise exception 'Safe merge failed: candidate_retailer_products_before should contain one row';
    end if;

    if history_record.snapshot->'candidate_retailer_products_before'->0->>'product_id' <> '1002' then
      raise exception 'Safe merge failed: candidate_retailer_products_before did not preserve pre-update product_id';
    end if;

    raise exception using
      errcode = 'PZ001',
      message = 'SAFE_MERGE_TEST_ROLLBACK';
  exception
    when sqlstate 'PZ001' then
      if sqlerrm <> 'SAFE_MERGE_TEST_ROLLBACK' then
        raise;
      end if;
  end;
end;
$outer$;

