do $$
declare
  history_record public.product_merge_history%rowtype;
  before_count integer;
  after_count integer;
begin
  perform public.reset_merge_decision_test_data();
  select count(*) into before_count from public.price_history;

  perform public.merge_products_with_decisions(
    1001,
    1002,
    public.valid_keep_canonical_decisions()
  );

  select count(*) into after_count from public.price_history;
  perform public.assert_eq_integer(after_count, before_count, 'keep canonical offer should preserve all price_history rows.');
  perform public.assert_true(not exists (select 1 from public.offers where id = 2002), 'keep canonical offer should delete candidate offer.');
  perform public.assert_true(exists (select 1 from public.offers where id = 2001 and product_id = 1001), 'keep canonical offer should keep canonical offer.');
  perform public.assert_true((select count(*) from public.price_history where offer_id = 2001) = 4, 'keep canonical offer should move candidate history to canonical offer.');
  perform public.assert_true(not exists (select 1 from public.offers where product_id = 1002), 'candidate should have no offers after successful merge.');
  perform public.assert_true(not exists (select 1 from public.retailer_products where product_id = 1002), 'candidate should have no mappings after successful merge.');
  perform public.assert_true(exists (select 1 from public.products where id = 1002 and is_active = false and merged_into_product_id = 1001 and merged_at is not null), 'candidate should be inactive after successful merge.');

  select *
  into history_record
  from public.product_merge_history
  where canonical_product_id = 1001
    and candidate_product_id = 1002;

  perform public.assert_eq_integer(history_record.price_history_preserved, 3, 'price_history_preserved should equal candidate price_history before merge.');
  perform public.assert_eq_integer(history_record.candidate_price_history_before, 3, 'candidate_price_history_before audit mismatch.');
  perform public.assert_eq_integer(history_record.canonical_price_history_before, 2, 'canonical_price_history_before audit mismatch.');
  perform public.assert_eq_integer(history_record.total_price_history_after, 5, 'total_price_history_after audit mismatch.');
  perform public.assert_eq_integer(history_record.price_history_reassigned, 2, 'price_history_reassigned audit mismatch.');
  perform public.assert_true(history_record.conflict_kept_offer_ids = array[2001]::bigint[], 'conflict_kept_offer_ids should include canonical offer.');
  perform public.assert_true(history_record.conflict_deleted_offer_ids = array[2002]::bigint[], 'conflict_deleted_offer_ids should include candidate offer.');
  perform public.assert_true(history_record.conflict_kept_retailer_product_ids = array[4001]::bigint[], 'conflict_kept_retailer_product_ids should include canonical mapping.');
  perform public.assert_true(history_record.conflict_deleted_retailer_product_ids = array[4002]::bigint[], 'conflict_deleted_retailer_product_ids should include candidate mapping.');
  perform public.assert_true(history_record.admin_decisions = public.valid_keep_canonical_decisions(), 'admin_decisions audit mismatch.');
  perform public.assert_true(jsonb_array_length(history_record.snapshot->'products_before') = 2, 'snapshot should include both products.');
  perform public.assert_true(jsonb_array_length(history_record.snapshot->'offers_before') = 4, 'snapshot should include all offers before merge.');
  perform public.assert_true(jsonb_array_length(history_record.snapshot->'retailer_products_before') = 4, 'snapshot should include all mappings before merge.');
  perform public.assert_true(jsonb_array_length(history_record.snapshot->'price_history_before') = 5, 'snapshot should include all price history before merge.');
end;
$$;

