create table public.products (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text,
  gtin text,
  brand text,
  category text,
  servings integer,
  description text,
  image text,
  price numeric,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.retailers (
  id bigint generated by default as identity primary key,
  name text not null,
  slug text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table public.offers (
  id bigint generated by default as identity primary key,
  product_id bigint not null references public.products(id) on delete cascade,
  retailer_id bigint null references public.retailers(id) on delete cascade,
  price numeric,
  shipping_cost numeric,
  url text,
  in_stock boolean,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint offers_product_retailer_unique unique (product_id, retailer_id),
  constraint offers_retailer_url_unique unique (retailer_id, url)
);

create table public.price_history (
  id bigint generated by default as identity primary key,
  offer_id bigint not null references public.offers(id) on delete cascade,
  price numeric,
  shipping_cost numeric,
  checked_at timestamptz not null default now(),
  created_at timestamptz not null default now()
);

create table public.retailer_products (
  id bigint generated by default as identity primary key,
  retailer_id bigint not null references public.retailers(id) on delete cascade,
  product_id bigint not null references public.products(id) on delete cascade,
  external_name text not null,
  external_slug text,
  external_gtin text,
  external_url text not null,
  match_method text,
  match_confidence numeric(5,2),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint retailer_products_retailer_url_unique unique (retailer_id, external_url)
);

