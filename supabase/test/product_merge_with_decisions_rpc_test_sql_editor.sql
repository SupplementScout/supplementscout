create or replace function public.assert_merge_with_decisions_blocked(
  canonical_id bigint,
  candidate_id bigint,
  decisions jsonb,
  expected_message text
)
returns void
language plpgsql
as $$
begin
  perform public.merge_products_with_decisions(canonical_id, candidate_id, decisions);
  raise exception 'Expected merge_products_with_decisions(%, %) to fail with: %',
    canonical_id,
    candidate_id,
    expected_message;
exception
  when others then
    if sqlerrm not like expected_message then
      raise exception 'Unexpected error for merge_products_with_decisions(%, %). Expected like "%", got "%"',
        canonical_id,
        candidate_id,
        expected_message,
        sqlerrm;
    end if;
end;
$$;

